import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(scriptDir, "..");
const outputFile = path.join(projectRoot, "src", "config", "blog.generated.ts");

const FEED_URL = "https://blog.arda.tr/rss.xml";
const POST_COUNT = 3;
const EXCERPT_MAX_LENGTH = 160;
const FETCH_TIMEOUT_MS = 15_000;

const NAMED_ENTITIES = { amp: "&", lt: "<", gt: ">", quot: '"', apos: "'", nbsp: " " };

function decodeEntities(value) {
  return value
    .replace(/&#x([0-9a-f]+);/gi, (_, hex) => String.fromCodePoint(Number.parseInt(hex, 16)))
    .replace(/&#(\d+);/g, (_, dec) => String.fromCodePoint(Number(dec)))
    .replace(/&(amp|lt|gt|quot|apos|nbsp);/g, (_, name) => NAMED_ENTITIES[name]);
}

/**
 * CDATA unwrapped, tags stripped, entities decoded, whitespace collapsed.
 * Feeds ship description HTML either raw (inside CDATA) or entity-encoded, so
 * decode before stripping — otherwise encoded tags survive — and decode once
 * more afterwards for double-encoded entities in the remaining text.
 */
function plainText(value) {
  const unwrapped = value.replace(/<!\[CDATA\[([\s\S]*?)\]\]>/g, "$1");
  return decodeEntities(decodeEntities(unwrapped).replace(/<[^>]+>/g, " "))
    .replace(/\s+/g, " ")
    .trim();
}

function truncate(value, maxLength) {
  if (value.length <= maxLength) {
    return value;
  }
  return `${value.slice(0, maxLength - 1).trimEnd()}…`;
}

function childText(item, tag) {
  const match = item.match(new RegExp(`<${tag}(?:\\s[^>]*)?>([\\s\\S]*?)</${tag}>`, "i"));
  return match ? plainText(match[1]) : "";
}

function formatDate(pubDate) {
  const date = new Date(pubDate);
  if (Number.isNaN(date.getTime())) {
    return "";
  }
  return date.toLocaleDateString("en-US", {
    year: "numeric",
    month: "short",
    day: "numeric",
    timeZone: "UTC",
  });
}

function parsePosts(xml) {
  const items = xml.match(/<item(?:\s[^>]*)?>[\s\S]*?<\/item>/gi) ?? [];
  return items
    .map((item) => ({
      title: childText(item, "title"),
      link: childText(item, "link"),
      date: formatDate(childText(item, "pubDate")),
      excerpt: truncate(childText(item, "description"), EXCERPT_MAX_LENGTH),
    }))
    .filter((post) => post.title !== "" && post.link !== "")
    .slice(0, POST_COUNT);
}

async function writeModule(posts) {
  const contents = `/* This file is generated by scripts/fetch-blog-posts.mjs. Do not edit. */
export type BlogPost = {
  title: string;
  link: string;
  date: string;
  excerpt: string;
};

export const blogPosts: BlogPost[] = ${JSON.stringify(posts, null, 2)};
`;

  await fs.writeFile(outputFile, contents, "utf8");
  console.log(`Blog posts module generated at ${outputFile} (${posts.length} post(s)).`);
}

async function main() {
  let posts = [];

  try {
    const response = await fetch(FEED_URL, {
      headers: { Accept: "application/rss+xml, application/xml, text/xml" },
      signal: AbortSignal.timeout(FETCH_TIMEOUT_MS),
    });
    if (!response.ok) {
      throw new Error(`unexpected status ${response.status}`);
    }
    posts = parsePosts(await response.text());
    if (posts.length === 0) {
      console.warn(`Blog feed at ${FEED_URL} yielded no parsable posts; the section will render nothing.`);
    }
  } catch (err) {
    // A blog outage must never break this site's build: fall back to an empty
    // posts array (the section renders nothing) and exit successfully.
    console.warn(`Blog posts fetch failed (${err instanceof Error ? err.message : err}); writing an empty posts array.`);
    posts = [];
  }

  await writeModule(posts);
}

main().catch(async (err) => {
  console.warn("Blog posts module generation failed:", err);
  try {
    await writeModule([]);
  } catch {
    // If even the fallback write fails, downstream typecheck/build reports the
    // missing module with a clear error.
  }
  process.exit(0);
});
