import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(scriptDir, "..");
const cssFile = path.join(projectRoot, "src", "index.css");
const siteConfigFile = path.join(projectRoot, "config", "site.config.json");
const outputFile = path.join(projectRoot, "config", "themes.json");

// alucard / paper / carbon are the outliers; every other theme is a dark scheme.
const THEME_KINDS = {
  alucard: "light",
  paper: "hc-light",
  carbon: "hc-dark",
};

const ACCENT_TOKEN_PREFIX = "theme-";
const REQUIRED_ACCENTS = ["cyan", "green", "pink", "yellow", "red"];
const REQUIRED_TOKENS = ["background", "foreground"];

/** Strip block comments so selector matching never trips on annotations. */
function stripComments(css) {
  return css.replace(/\/\*[\s\S]*?\*\//g, "");
}

/**
 * Collect top-level `selector { body }` rules. At-rules (`@layer`, `@media`,
 * `@tailwind`, ...) are skipped; their nested braces are consumed so the scan
 * stays aligned.
 */
function topLevelRules(css) {
  const rules = [];
  let cursor = 0;

  while (cursor < css.length) {
    const braceIndex = css.indexOf("{", cursor);
    if (braceIndex === -1) {
      break;
    }

    const prelude = css.slice(cursor, braceIndex).trim();
    // The prelude may still contain brace-less at-rules such as `@tailwind
    // base;` — only the text after the last `;` is this rule's selector.
    const selectorText = prelude.slice(prelude.lastIndexOf(";") + 1).trim();

    let depth = 1;
    let bodyEnd = braceIndex + 1;
    while (bodyEnd < css.length && depth > 0) {
      if (css[bodyEnd] === "{") depth += 1;
      if (css[bodyEnd] === "}") depth -= 1;
      bodyEnd += 1;
    }

    if (!selectorText.startsWith("@")) {
      rules.push({
        selectors: selectorText.split(",").map((selector) => selector.trim()),
        body: css.slice(braceIndex + 1, bodyEnd - 1),
      });
    }

    cursor = bodyEnd;
  }

  return rules;
}

/**
 * The first rule whose selector list contains a plain `.<id>` is the canonical
 * token block. The default theme is additionally bound to `:root` (as in
 * `:root, .dracula-pro`), and later compound selectors (`.paper, .carbon`
 * high-contrast overrides, `.alucard .theme-invert-logo`, ...) never match a
 * bare `.<id>` first.
 */
function themeBlock(rules, id) {
  const rule = rules.find(({ selectors }) => selectors.includes(`.${id}`));
  if (!rule) {
    throw new Error(`No theme block found for .${id} in src/index.css`);
  }
  return rule.body;
}

/** All custom properties in a block, keyed without the leading dashes, values raw. */
function parseTokens(body) {
  const tokens = {};
  for (const declaration of body.split(";")) {
    const match = declaration.match(/^\s*--([\w-]+)\s*:\s*([\s\S]+?)\s*$/);
    if (match) {
      tokens[match[1]] = match[2];
    }
  }
  return tokens;
}

function accentsOf(tokens, id) {
  const accents = {};
  for (const [name, value] of Object.entries(tokens)) {
    if (name.startsWith(ACCENT_TOKEN_PREFIX)) {
      accents[name.slice(ACCENT_TOKEN_PREFIX.length)] = value;
    }
  }
  for (const required of REQUIRED_ACCENTS) {
    if (!(required in accents)) {
      throw new Error(`Theme "${id}" is missing --${ACCENT_TOKEN_PREFIX}${required}`);
    }
  }
  return accents;
}

async function main() {
  const css = stripComments(await fs.readFile(cssFile, "utf8"));
  const rules = topLevelRules(css);
  const siteConfig = JSON.parse(await fs.readFile(siteConfigFile, "utf8"));

  // Menu order comes from config/site.config.json; tokens come from the CSS.
  const themes = siteConfig.themes.map(({ id, name, hint }) => {
    const tokens = parseTokens(themeBlock(rules, id));
    for (const required of REQUIRED_TOKENS) {
      if (!(required in tokens)) {
        throw new Error(`Theme "${id}" is missing --${required}`);
      }
    }
    return {
      id,
      name,
      hint,
      kind: THEME_KINDS[id] ?? "dark",
      tokens,
      accents: accentsOf(tokens, id),
    };
  });

  const contract = {
    $comment:
      "Generated by scripts/generate-themes-contract.mjs from src/index.css + config/site.config.json. Do not hand-edit.",
    version: 1,
    themes,
  };

  await fs.writeFile(outputFile, `${JSON.stringify(contract, null, 2)}\n`, "utf8");
  console.log(`Theme contract generated at ${outputFile} (${themes.length} themes).`);
}

main().catch((err) => {
  console.error("Theme contract generation failed:", err);
  process.exit(1);
});
