import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(scriptDir, "..");
const sourceFile = path.join(projectRoot, "config", "site.config.json");
const outputFile = path.join(projectRoot, "src", "config", "site.generated.ts");

async function main() {
  const siteConfig = JSON.parse(await fs.readFile(sourceFile, "utf8"));
  const contents = `/* This file is generated by scripts/generate-site-config-module.mjs. */
export const siteConfig = ${JSON.stringify(siteConfig, null, 2)} as const;

export const site = siteConfig.site;
export const pages = siteConfig.pages;
export const sectionIds = siteConfig.sections;
export const themes = siteConfig.themes;
`;

  await fs.writeFile(outputFile, contents, "utf8");
  console.log(`Site config module generated at ${outputFile}`);
}

main().catch((err) => {
  console.error("Site config module generation failed:", err);
  process.exit(1);
});
