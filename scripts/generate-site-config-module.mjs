import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(scriptDir, "..");
const inputPath = path.join(projectRoot, "config", "site.config.json");
const outputPath = path.join(projectRoot, "src", "site_config.gleam");

function escapeGleamString(value) {
  return JSON.stringify(value);
}

function renderSite(site) {
  return `Site(
  name: ${escapeGleamString(site.name)},
  title: ${escapeGleamString(site.title)},
  description: ${escapeGleamString(site.description)},
  url: ${escapeGleamString(site.url)},
  og_image_path: ${escapeGleamString(site.ogImagePath)},
  og_image_width: ${site.ogImageWidth},
  og_image_height: ${site.ogImageHeight},
)`;
}

function renderPage(page) {
  return `  Page(
    path: ${escapeGleamString(page.path)},
    priority: ${escapeGleamString(page.priority)},
    changefreq: ${escapeGleamString(page.changefreq)},
  )`;
}

function renderTheme(theme) {
  return `  Theme(
    id: ${escapeGleamString(theme.id)},
    name: ${escapeGleamString(theme.name)},
    color: ${escapeGleamString(theme.color)},
  )`;
}

async function main() {
  const rawConfig = await fs.readFile(inputPath, "utf8");
  const siteConfig = JSON.parse(rawConfig);
  const [defaultTheme] = siteConfig.themes;

  const output = `// This file is generated by scripts/generate-site-config-module.mjs.
// Do not edit it manually.

pub type Site {
  Site(
    name: String,
    title: String,
    description: String,
    url: String,
    og_image_path: String,
    og_image_width: Int,
    og_image_height: Int,
  )
}

pub type Page {
  Page(path: String, priority: String, changefreq: String)
}

pub type SectionIds {
  SectionIds(about: String, portfolio: String, music: String)
}

pub type Theme {
  Theme(id: String, name: String, color: String)
}

pub const site = ${renderSite(siteConfig.site)}

pub const pages = [
${siteConfig.pages.map(renderPage).join(",\n")}
]

pub const section_ids = SectionIds(
  about: ${escapeGleamString(siteConfig.sections.about)},
  portfolio: ${escapeGleamString(siteConfig.sections.portfolio)},
  music: ${escapeGleamString(siteConfig.sections.music)},
)

pub const default_theme_id = ${escapeGleamString(defaultTheme.id)}

pub const default_theme = Theme(
  id: ${escapeGleamString(defaultTheme.id)},
  name: ${escapeGleamString(defaultTheme.name)},
  color: ${escapeGleamString(defaultTheme.color)},
)

pub const themes = [
${siteConfig.themes.map(renderTheme).join(",\n")}
]
`;

  await fs.writeFile(outputPath, output, "utf8");
}

main().catch((error) => {
  console.error("Failed to generate src/site_config.gleam:", error);
  process.exit(1);
});
